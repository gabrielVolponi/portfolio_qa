Cypress.Commands.add('validarSection', (idSection,dataArgument) => {
    console.log(idSection)
    cy.get(idSection).as('linkNavbar').click().then(() => {
        cy.url().should('contains', `https://gabrielvolponi.vercel.app/${idSection}`)
      })
})
